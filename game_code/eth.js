async function mintAfterGame(nrOfTokens) {
    // ethereum.request({ method: 'eth_accounts' }) // imho, more appropriate for Metamask usage, returns an array, first index = selected address
    web3.eth.getAccounts().then(listOfAddress => {
      // 'mint' is the function call in the Token contract
      contractTokenErc20.methods.mint(listOfAddress[0], nrOfTokens).send({from: listOfAddress[0]})
      .on('receipt', receipt => {
        alert("Transaction Complete");
      });
    });
}

async function buy(id) {
    web3.eth.getAccounts().then(listOfAddress => {
      let options = {
        from: listOfAddress[0],
        value: 0
      };

      if (id == 1) {
        options.value = 100000000000000;
      }
      else if (id == 2){
        options.value = 200000000000000;
      }
      else if (id == 3){
        options.value = 300000000000000;
      }

      contractMarketplace.methods.buyToken(id).send(options)
      .on('receipt', receipt => {
        alert("Transaction Complete");
      });
    });
}

async function getPlayerItems(callback) {
    web3.eth.getAccounts().then(listOfAddress => {
      let item1 = contractTokenErc1155.methods.balanceOf(listOfAddress[0], 1).call();
      let item2 = contractTokenErc1155.methods.balanceOf(listOfAddress[0], 2).call();
      let item3 = contractTokenErc1155.methods.balanceOf(listOfAddress[0], 3).call();

      // wait all 3 promises to be resolved before taking action
      Promise.all([item1, item2, item3]).then(values => {
        console.log(values);

        let numberOfTalisman = values[0];
        let numberOfBoots = values[1];
        let numberOfCape = values[2];

        if(values[0] > 0) {
          COIN_GENERATION_DELAY = COIN_GENERATION_DELAY * Math.pow(0.75, numberOfTalisman);
        }
        if(values[1] > 0) {
          PLAYER_SPEED = PLAYER_SPEED * Math.pow(1.3, numberOfBoots);
        }
        if(values[2] > 0) {
          GAME_SECOND = GAME_SECOND * Math.pow(1.5, numberOfCape);
        }

        callback();
      });
    });
}